#include "includes/libft.h"

void	*ft_memalloc(size_t size)
{
	void	*fresh;
	int		i;

	i = 0;
	fresh = (void *)malloc(size);
	if (!fresh)
		return (NULL);
	while (size--)
	{
		((unsigned char *)fresh)[i] = 0;
		++i;
	}
	return (fresh);
}
