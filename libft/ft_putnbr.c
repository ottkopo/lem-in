#include "includes/libft.h"

void	ft_putnbr(int n)
{
	if (n == 2147483647)
	{
		write(1, "2147483647", 10);
		return ;
	}
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		write(1, "-", 1);
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		n = n % 10;
	}
	if (n < 10)
	{
		n = n + '0';
		write(1, &n, 1);
	}
}
